export function listStore(name: keyof typeof stores): Row[] {
  return Array.from(stores[name].values());
}

export function getStore(name: keyof typeof stores, id: number): Row | undefined {
  return stores[name].get(id);
}

export function upsertStore(name: keyof typeof stores, input: MockBody, id?: number): Row {
  const now = nowIso();
  const rowId = id ?? allocateId();
  const previous = stores[name].get(rowId) ?? ({} as Row);
  const next: Row = {
    ...previous,
    ...input,
    id: rowId,
    date_added: previous.date_added ?? now,
    date_modified: now,
  };
  stores[name].set(rowId, next);
  return next;
}

export function deleteStore(name: keyof typeof stores, id: number): boolean {
  return stores[name].delete(id);
}
